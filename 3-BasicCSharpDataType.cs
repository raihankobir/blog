using System;

namespace BasicCSharpDataType
{
    class Program
    {
        static void Main(string[] args)
        {
            int a = 2;
            float b = 2.999f;
            double c = 5.787349893938d;
            String d = "hello";
            char e = 'A';


            Console.WriteLine("Int Data Type    : " + a);
            Console.WriteLine("Float Data Type  : " + b);
            Console.WriteLine("Double Data Type : " + c);
            Console.WriteLine("String Data Type : " + d);
            Console.WriteLine("Char Data Type   : " + e);

            Console.ReadKey();


        }
    }
}
